// ฟังก์ชันช่วยเหลือ
export const getDistrictsByProvinceId = (provinceId) => {
  return thaiDistricts[provinceId] || [];
};

export const getSubdistrictsByDistrictId = (districtId) => {
  return thaiSubdistricts[districtId] || [];
};

export const getProvinceById = (provinceId) => {
  return thaiProvinces.find(province => province.id === provinceId);
};

export const getDistrictById = (districtId) => {
  for (const districts of Object.values(thaiDistricts)) {
    const district = districts.find(d => d.id === districtId);
    if (district) return district;
  }
  return null;
};

export const getSubdistrictById = (subdistrictId) => {
  for (const subdistricts of Object.values(thaiSubdistricts)) {
    const subdistrict = subdistricts.find(s => s.id === subdistrictId);
    if (subdistrict) return subdistrict;
  }
  return null;
};